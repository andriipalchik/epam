# Якщо змінна a більша нуля і менша 5-ти, то вивести "Вірно", інакше вивести "Невірно".
# Перевірте роботу скрипта при a, зі значеннями - 5, 0, -3, 2

def check(a)
    (a > 0 && a < 5) ? true : false
end

puts ((check(5)) ? "Вірно" : "Невірно")
puts ((check(0)) ? "Вірно" : "Невірно")
puts ((check(-3)) ? "Вірно" : "Невірно")
puts ((check(2)) ? "Вірно" : "Невірно")