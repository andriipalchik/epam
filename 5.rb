# В змінній $year збігається рік. Визначте чи є він високосним.
# Рік буде високосним в двох випадках: або він ділиться на 4, але при цьому не ділиться на 100,
# або ділиться на 400.

def isLeap(year)
    (year % 4 == 0 && year % 100 != 0) || (year % 400 == 0) ? true : false
end

puts isLeap(2012) ? "високосний" : "не високосний"
puts isLeap(2017) ? "високосний" : "не високосний"
puts isLeap(2020) ? "високосний" : "не високосний"